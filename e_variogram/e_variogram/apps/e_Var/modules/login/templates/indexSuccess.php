<h2 class="red"> INTRODUCTION </h2>
<hr/>
<p align="justify" style="line-height:2"> Mapping and predicting environmental properties such as soil nutrients, vegetation characteristics, weather and climate variables from point observations require modelling of the spatial variability of these properties. In geostatistics, the spatial variability of environmental properties is characterized by the variogram.<br/> In this web-based elicitation procedure you, the expert, will jointly with other experts define the variogram of a specific environmental variable for a specific area. The elicitation is set up such that no prerequisite knowledge about geostatistics is required. It is only your expert knowledge about the spatial variation of the variable that is needed. </p>
